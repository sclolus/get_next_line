#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	u;

	i = 0;
	u = -1;
	while (s1[i])
		i++;
	while (s2[++u])
		s1[i + u] = s2[u];
	s1[i + u] = '\0';
	return (s1);
}
