#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	u;

	i = 0;
	u = 0;
	while (s1[i])
		i++;
	while (s2[u] && u < n)
	{
		s1[i + u] = s2[u];
		u++;
	}
	s1[i + u] = '\0';
	return (s1);
}
